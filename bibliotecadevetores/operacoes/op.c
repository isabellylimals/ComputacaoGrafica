void soma(int a[], int v[], int resultado[] ){
    resultado[0]= a[0]+v[0];
    resultado[1]= a[1]+v[1];

}
void subtracao(int a[], int v[], int resultado[]){
    resultado[0]= a[0]-v[0];
    resultado[1]= a[1]-v[1];
}
